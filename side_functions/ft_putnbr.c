#include "../includes/ft_printf.h"

int	ft_putnbr(int n)
{
	char	a;
	int		i;
	
	i = 0;
	/* if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	} */
	if (n < 0)
	{
		n *= -1;
		write(1, "-", 1);
		i++;
	}
	if (n <= 9)
	{
		a = 48 + n;
		write(1, &a, 1);
		i = 1;
	}
	else
	{
		i += ft_putnbr(n / 10);
		i += ft_putnbr(n % 10);
	}
	return (i);
}
